// Command gen regenerates table.gen.go from browsers.html-data.json
// (@vscode/web-custom-data, MIT — see LICENSE.vendored) and htmx-data.json
// (transcribed from https://htmx.org/reference/, same custom-data schema).
package main

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/format"
	"log"
	"os"
	"sort"
)

// flexDesc decodes both description encodings used by the custom-data
// schema: a bare string, or {"kind": "markdown", "value": "..."}.
type flexDesc struct{ Value string }

func (d *flexDesc) UnmarshalJSON(b []byte) error {
	var s string
	if err := json.Unmarshal(b, &s); err == nil {
		d.Value = s
		return nil
	}
	var o struct {
		Value string `json:"value"`
	}
	if err := json.Unmarshal(b, &o); err != nil {
		return err
	}
	d.Value = o.Value
	return nil
}

// rawRef mirrors a references[] entry.
type rawRef struct {
	Name string `json:"name"`
	URL  string `json:"url"`
}

// rawValue mirrors a valueSets[].values[] entry.
type rawValue struct {
	Name        string   `json:"name"`
	Description flexDesc `json:"description"`
}

// rawValueSet mirrors a top-level valueSets[] entry.
type rawValueSet struct {
	Name   string     `json:"name"`
	Values []rawValue `json:"values"`
}

// rawAttribute mirrors a tags[].attributes[] or globalAttributes[] entry.
type rawAttribute struct {
	Name        string     `json:"name"`
	Description flexDesc   `json:"description"`
	ValueSet    string     `json:"valueSet"`
	Values      []rawValue `json:"values"`
	References  []rawRef   `json:"references"`
}

// rawTag mirrors a top-level tags[] entry.
type rawTag struct {
	Name        string         `json:"name"`
	Description flexDesc       `json:"description"`
	Attributes  []rawAttribute `json:"attributes"`
	References  []rawRef       `json:"references"`
}

// rawData mirrors the whole custom-data document.
type rawData struct {
	Version          float64        `json:"version"`
	Tags             []rawTag       `json:"tags"`
	GlobalAttributes []rawAttribute `json:"globalAttributes"`
	ValueSets        []rawValueSet  `json:"valueSets"`
}

// docForRef renders desc plus a trailing markdown link for the first
// reference named refName, if present.
func docForRef(desc flexDesc, refs []rawRef, refName string) string {
	doc := desc.Value
	for _, r := range refs {
		if r.Name == refName {
			if doc != "" {
				doc += "\n\n"
			}
			doc += "[" + refName + "](" + r.URL + ")"
			break
		}
	}
	return doc
}

// docFor renders desc plus the MDN Reference link, per the vscode
// browsers.html-data.json convention.
func docFor(desc flexDesc, refs []rawRef) string {
	return docForRef(desc, refs, "MDN Reference")
}

// outValue/outAttribute/outTag are the emitted (Go source) shapes.

type outValue struct {
	Name string
	Doc  string
}

type outAttribute struct {
	Name     string
	Doc      string
	ValueSet string
}

type outTag struct {
	Name  string
	Doc   string
	Attrs []outAttribute
}

func loadData(path string) rawData {
	b, err := os.ReadFile(path)
	if err != nil {
		log.Fatalf("read %s: %v", path, err)
	}
	var d rawData
	if err := json.Unmarshal(b, &d); err != nil {
		log.Fatalf("parse %s: %v", path, err)
	}
	return d
}

func convertAttributes(attrs []rawAttribute, refName string) []outAttribute {
	out := make([]outAttribute, len(attrs))
	for i, a := range attrs {
		out[i] = outAttribute{
			Name:     a.Name,
			Doc:      docForRef(a.Description, a.References, refName),
			ValueSet: a.ValueSet,
		}
	}
	sort.Slice(out, func(i, j int) bool { return out[i].Name < out[j].Name })
	return out
}

func main() {
	if len(os.Args) > 1 {
		log.Fatalf("usage: gen (run from internal/htmldata via go generate)")
	}

	vscode := loadData("browsers.html-data.json")
	htmx := loadData("htmx-data.json")

	tags := make([]outTag, len(vscode.Tags))
	for i, t := range vscode.Tags {
		tags[i] = outTag{
			Name:  t.Name,
			Doc:   docFor(t.Description, t.References),
			Attrs: convertAttributes(t.Attributes, "MDN Reference"),
		}
	}
	sort.Slice(tags, func(i, j int) bool { return tags[i].Name < tags[j].Name })

	globalAttrs := convertAttributes(vscode.GlobalAttributes, "MDN Reference")

	valueSetNames := make([]string, 0, len(vscode.ValueSets))
	valueSets := make(map[string][]outValue, len(vscode.ValueSets))
	for _, vs := range vscode.ValueSets {
		values := make([]outValue, len(vs.Values))
		for i, v := range vs.Values {
			values[i] = outValue{
				Name: v.Name,
				Doc:  v.Description.Value,
			}
		}
		valueSets[vs.Name] = values
		valueSetNames = append(valueSetNames, vs.Name)
	}
	sort.Strings(valueSetNames)

	htmxAttrs := convertAttributes(htmx.GlobalAttributes, "htmx Reference")

	var buf bytes.Buffer
	buf.WriteString("// Code generated by internal/htmldata/gen. DO NOT EDIT.\n\n")
	buf.WriteString("package htmldata\n\n")

	buf.WriteString("// Tags is the full HTML element set, sorted by Name.\n")
	buf.WriteString("var Tags = []Tag{\n")
	for _, t := range tags {
		fmt.Fprintf(&buf, "\t{\n\t\tName: %q,\n\t\tDoc: %q,\n\t\tAttrs: []Attribute{\n", t.Name, t.Doc)
		for _, a := range t.Attrs {
			fmt.Fprintf(&buf, "\t\t\t{Name: %q, Doc: %q, ValueSet: %q},\n", a.Name, a.Doc, a.ValueSet)
		}
		buf.WriteString("\t\t},\n\t},\n")
	}
	buf.WriteString("}\n\n")

	buf.WriteString("// GlobalAttributes apply to every HTML element, sorted by Name.\n")
	buf.WriteString("var GlobalAttributes = []Attribute{\n")
	for _, a := range globalAttrs {
		fmt.Fprintf(&buf, "\t{Name: %q, Doc: %q, ValueSet: %q},\n", a.Name, a.Doc, a.ValueSet)
	}
	buf.WriteString("}\n\n")

	buf.WriteString("// HTMXAttributes are the hx-* attributes transcribed from\n")
	buf.WriteString("// https://htmx.org/reference/ (core + additional attribute tables),\n")
	buf.WriteString("// sorted by Name.\n")
	buf.WriteString("var HTMXAttributes = []Attribute{\n")
	for _, a := range htmxAttrs {
		fmt.Fprintf(&buf, "\t{Name: %q, Doc: %q, ValueSet: %q},\n", a.Name, a.Doc, a.ValueSet)
	}
	buf.WriteString("}\n\n")

	buf.WriteString("// ValueSets maps a valueSet key (as referenced by Attribute.ValueSet) to\n")
	buf.WriteString("// its enumerated members.\n")
	buf.WriteString("var ValueSets = map[string][]Value{\n")
	for _, name := range valueSetNames {
		fmt.Fprintf(&buf, "\t%q: {\n", name)
		for _, v := range valueSets[name] {
			fmt.Fprintf(&buf, "\t\t{Name: %q, Doc: %q},\n", v.Name, v.Doc)
		}
		buf.WriteString("\t},\n")
	}
	buf.WriteString("}\n")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("gofmt generated source: %v", err)
	}

	if err := os.WriteFile("table.gen.go", formatted, 0o644); err != nil {
		log.Fatalf("write table.gen.go: %v", err)
	}
}
