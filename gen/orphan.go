package gen

import (
	"bufio"
	"io/fs"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

// gsxGeneratedHeader is the exact first line of every gsx-owned .x.go, written
// by both internal/codegen/emit.go (successful generation) and poison.go
// (poisoned failure output). It is the ONLY test used to decide a .x.go is
// safe to delete: removeOrphanXgo and sweepOrphanDirs never delete a file on
// any other basis (no name heuristics, no directory-emptiness heuristics). See
// docs/superpowers/specs/2026-07-10-poison-xgo-on-failure-design.md, "Orphaned
// .x.go".
const gsxGeneratedHeader = "// Code generated by gsx; DO NOT EDIT."

// removeOrphanXgo deletes every gsx-owned .x.go directly in dir whose
// corresponding <base>.gsx no longer exists in dir. A .x.go is gsx-owned iff
// its first line is EXACTLY gsxGeneratedHeader (checked via isGsxOwned) — a
// hand-written file that happens to be named *.x.go, or a gsx-owned .x.go
// whose .gsx sibling still exists (even a broken one; overwriting that is
// poison's job, not delete's), is never touched. Returns the absolute paths
// removed, sorted. A missing dir is not an error (nothing to sweep).
func removeOrphanXgo(dir string) (removed []string, err error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		if os.IsNotExist(err) {
			return nil, nil
		}
		return nil, err
	}
	gsxBases := make(map[string]bool, len(entries))
	for _, e := range entries {
		if !e.IsDir() && strings.HasSuffix(e.Name(), ".gsx") {
			gsxBases[strings.TrimSuffix(e.Name(), ".gsx")] = true
		}
	}
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".x.go") {
			continue
		}
		base := strings.TrimSuffix(e.Name(), ".x.go")
		if gsxBases[base] {
			continue // .gsx still present — poison's territory, not delete's
		}
		path := filepath.Join(dir, e.Name())
		owned, oerr := isGsxOwned(path)
		if oerr != nil || !owned {
			continue // unreadable, or not gsx-owned — never touch
		}
		if rerr := os.Remove(path); rerr != nil {
			return removed, rerr
		}
		removed = append(removed, path)
	}
	sort.Strings(removed)
	return removed, nil
}

// isGsxOwned reports whether path's first line is exactly gsxGeneratedHeader —
// the narrow ownership test every deletion in this file is gated on.
func isGsxOwned(path string) (bool, error) {
	f, err := os.Open(path)
	if err != nil {
		return false, err
	}
	defer f.Close()
	sc := bufio.NewScanner(f)
	if !sc.Scan() {
		return false, sc.Err()
	}
	return sc.Text() == gsxGeneratedHeader, nil
}

// sweepOrphanDirs is the walk-level complement to the per-dir sweep in
// writeDirOutcome/regenDir: those only run for dirs discovery DID return in
// kept (dirs that still directly contain at least one .gsx), so a directory
// whose only .gsx was just deleted drops out of discovery entirely and would
// otherwise never be swept for its now-orphaned .x.go. sweepOrphanDirs walks
// each of paths and calls removeOrphanXgo on every directory NOT in kept.
//
// Because the two sweeps partition every directory by "still has a .gsx"
// (kept, handled by the per-dir sweep) vs "doesn't" (handled here), no
// directory is ever swept by both — there is no double-report between them.
func sweepOrphanDirs(paths, kept []string) (removed []string, err error) {
	keep := make(map[string]bool, len(kept))
	for _, d := range kept {
		keep[d] = true
	}
	visited := map[string]bool{}
	sweepOne := func(dir string) error {
		dir = filepath.Clean(dir)
		if keep[dir] || visited[dir] {
			return nil
		}
		visited[dir] = true
		rem, rerr := removeOrphanXgo(dir)
		if rerr != nil {
			return rerr
		}
		removed = append(removed, rem...)
		return nil
	}
	for _, p := range paths {
		info, statErr := os.Stat(p)
		if statErr != nil {
			continue // discoverDirs already validated existence; be conservative here
		}
		if !info.IsDir() {
			continue // a single .gsx-file path's dir is necessarily in kept
		}
		walkErr := filepath.WalkDir(p, func(path string, d fs.DirEntry, werr error) error {
			if werr != nil {
				return werr
			}
			if !d.IsDir() {
				return nil
			}
			// Mirror discoverDirs' own skip set (walkForGsx/shouldSkipDir) so this
			// walk never visits a directory batch discovery itself would not have
			// descended into.
			if path != p && shouldSkipDir(d.Name()) {
				return filepath.SkipDir
			}
			abs, aerr := filepath.Abs(path)
			if aerr != nil {
				return aerr
			}
			return sweepOne(abs)
		})
		if walkErr != nil {
			return removed, walkErr
		}
	}
	sort.Strings(removed)
	return removed, nil
}
