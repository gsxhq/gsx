// goBuildExpectFail runs `go build ./...` in dir expecting failure, and
// returns the combined output for assertions.
func goBuildExpectFail(t *testing.T, dir string) string {
	t.Helper()
	cmd := exec.Command("go", "build", "./...")
	cmd.Dir = dir
	out, err := cmd.CombinedOutput()
	if err == nil {
		t.Fatalf("go build ./... unexpectedly succeeded:\n%s", out)
	}
	return string(out)
}

const brokenComponent = `package views

component Broken() {
	<p>{undefinedSymbol}</p>
}
`

const fixedComponent = `package views

component Broken() {
	<p>{"fixed"}</p>
}
`

// TestPoison_E2E_BuildFailsAndPointsAtGsx pins the invariant: a failed
// generate leaves the package unbuildable, and the compile error carries the
// GSX_GENERATION_FAILED identifier redirected to the .gsx position.
func TestPoison_E2E_BuildFailsAndPointsAtGsx(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison1")
	views := filepath.Join(mod, "views")
	writeFile(t, views, "broken.gsx", brokenComponent)

	// First generate succeeds? No — the component references undefinedSymbol.
	if _, err := Generate([]string{mod}); err == nil {
		t.Fatal("expected Generate to report failure")
	}

	xgo := filepath.Join(views, "broken.x.go")
	b, err := os.ReadFile(xgo)
	if err != nil {
		t.Fatalf("poison .x.go not written: %v", err)
	}
	src := string(b)
	if !strings.Contains(src, "GSX GENERATION FAILED for broken.gsx") {
		t.Errorf("poison file missing failure banner:\n%s", src)
	}
	if !strings.Contains(src, "// Code generated by gsx; DO NOT EDIT.") {
		t.Errorf("poison file missing gsx header:\n%s", src)
	}

	out := goBuildExpectFail(t, mod)
	if !strings.Contains(out, "GSX_GENERATION_FAILED__see_broken_gsx") {
		t.Errorf("go build error does not carry the poison identifier:\n%s", out)
	}
	if !strings.Contains(out, "broken.gsx:") {
		t.Errorf("go build error not //line-redirected to the .gsx:\n%s", out)
	}
}

// TestPoison_E2E_PerFileBlame: only the broken .gsx is poisoned; a clean
// sibling keeps its previous output — safe because the poisoned sibling makes
// the whole package unbuildable.
func TestPoison_E2E_PerFileBlame(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison2")
	views := filepath.Join(mod, "views")
	writeFile(t, views, "good.gsx", "package views\n\ncomponent Good() {\n\t<p>ok</p>\n}\n")
	writeFile(t, views, "broken.gsx", "package views\n\ncomponent Broken() {\n\t<p>ok</p>\n}\n")

	// Round 1: both valid — clean generate + build.
	if _, err := Generate([]string{mod}); err != nil {
		t.Fatalf("clean generate failed: %v", err)
	}
	goBuild(t, mod)
	goodBefore, err := os.ReadFile(filepath.Join(views, "good.x.go"))
	if err != nil {
		t.Fatal(err)
	}

	// Round 2: break only broken.gsx.
	writeFile(t, views, "broken.gsx", brokenComponent)
	if _, err := Generate([]string{mod}); err == nil {
		t.Fatal("expected Generate to report failure")
	}

	goodAfter, err := os.ReadFile(filepath.Join(views, "good.x.go"))
	if err != nil {
		t.Fatal(err)
	}
	if string(goodBefore) != string(goodAfter) {
		t.Error("clean sibling good.x.go was rewritten on a failure it did not cause")
	}
	if b, _ := os.ReadFile(filepath.Join(views, "broken.x.go")); !strings.Contains(string(b), "GSX GENERATION FAILED") {
		t.Error("broken.x.go not poisoned")
	}
	// The stale-but-clean sibling can't be silently used: the package no longer builds.
	goBuildExpectFail(t, mod)
}

// TestPoison_E2E_RecoveryAndIdempotence: fixing the .gsx overwrites the poison
// with clean output; re-running a failing generate writes nothing new.
func TestPoison_E2E_RecoveryAndIdempotence(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison3")
	views := filepath.Join(mod, "views")
	writeFile(t, views, "broken.gsx", brokenComponent)

	if _, err := Generate([]string{mod}); err == nil {
		t.Fatal("expected failure")
	}
	poison1, err := os.ReadFile(filepath.Join(views, "broken.x.go"))
	if err != nil {
		t.Fatal(err)
	}

	// Idempotence: same failure again → byte-identical, hash-gated no write.
	res, err := Generate([]string{mod})
	if err == nil {
		t.Fatal("expected failure")
	}
	for _, w := range res.Written {
		if strings.HasSuffix(w, "broken.x.go") {
			t.Error("second failing run rewrote an identical poison file")
		}
	}
	poison2, err := os.ReadFile(filepath.Join(views, "broken.x.go"))
	if err != nil {
		t.Fatal(err)
	}
	if string(poison1) != string(poison2) {
		t.Error("poison bytes changed between identical failing runs")
	}

	// Recovery: fix → clean output + clean build (the gqlgen trap: the poison
	// on disk must not block regeneration).
	writeFile(t, views, "broken.gsx", fixedComponent)
	if _, err := Generate([]string{mod}); err != nil {
		t.Fatalf("generate after fix failed (sticky poison — the gqlgen trap): %v", err)
	}
	if b, _ := os.ReadFile(filepath.Join(views, "broken.x.go")); strings.Contains(string(b), "GSX GENERATION FAILED") {
		t.Error("poison not overwritten by clean output after fix")
	}
	goBuild(t, mod)
}

// TestPoison_E2E_NewFileNoPriorXgo: a brand-new broken .gsx gets a poison
// .x.go even though none existed before.
func TestPoison_E2E_NewFileNoPriorXgo(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison4")
	views := filepath.Join(mod, "views")
	writeFile(t, views, "fresh.gsx", strings.ReplaceAll(brokenComponent, "Broken", "Fresh"))

	if _, err := Generate([]string{mod}); err == nil {
		t.Fatal("expected failure")
	}
	if b, err := os.ReadFile(filepath.Join(views, "fresh.x.go")); err != nil || !strings.Contains(string(b), "GSX GENERATION FAILED") {
		t.Errorf("new broken .gsx did not get a poison .x.go (err=%v)", err)
	}
}

// TestPoison_LSPIgnoresPoisonOnDisk: a stale poison .x.go next to a now-valid
// .gsx must not produce LSP diagnostics — the analyzer replaces real .x.go
// with synthetic skeletons keyed by the .gsx path (module_importer.go:964),
// so a poison file left over from an earlier failing generate is invisible to
// the analysis pass; it is never read as authoritative. This is the "gqlgen
// trap" from the LSP side: gsx's own tooling must not choke on a poison file
// it previously wrote.
func TestPoison_LSPIgnoresPoisonOnDisk(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison5")
	gsxPath := filepath.Join(mod, "page.gsx")
	pageGsx := "package x\n\ncomponent Page() {\n\t<div>hi</div>\n}\n"
	writeFile(t, mod, "page.gsx", pageGsx)
	// Simulate "fixed .gsx, stale poison on disk" (user fixed the file but
	// hasn't regenerated). Structurally matches poisonFile's real output
	// (poison.go): header, GSX GENERATION FAILED banner, //line redirect, and
	// the undefined GSX_GENERATION_FAILED__see_<ident> identifier.
	writeFile(t, mod, "page.x.go", `// Code generated by gsx; DO NOT EDIT.

package x

// GSX GENERATION FAILED for page.gsx. Fix the errors below and re-run gsx generate.
//
//	page.gsx:3:1: error: boom

//line page.gsx:3:1
var _ = GSX_GENERATION_FAILED__see_page_gsx
`)
	uri := "file://" + gsxPath

	in := frameMsg(t, map[string]any{"jsonrpc": "2.0", "id": 1, "method": "initialize", "params": map[string]any{}})
	in += frameMsg(t, map[string]any{
		"jsonrpc": "2.0", "method": "textDocument/didOpen",
		"params": map[string]any{"textDocument": map[string]any{
			"uri": uri, "version": 1, "text": pageGsx,
		}},
	})
	in += frameMsg(t, map[string]any{"jsonrpc": "2.0", "method": "exit"})

	var out, errBuf bytes.Buffer
	if code := runLSP(strings.NewReader(in), &out, &errBuf, config{}, nil); code != 0 {
		t.Fatalf("runLSP exit = %d, stderr = %s", code, errBuf.String())
	}
	// The valid .gsx must analyze clean: no diagnostic may mention the poison.
	if strings.Contains(out.String(), "GSX_GENERATION_FAILED") {
		t.Fatalf("LSP surfaced the on-disk poison for a valid .gsx (sticky poison):\n%s", out.String())
	}
}

// TestPoison_AttrsOnlyProbeUnaffected pins the same self-reference safety
// property as TestPoison_LSPIgnoresPoisonOnDisk, but for Generate's
// cross-package probe-based go/types recognition path instead of the LSP.
//
// The task brief for this test asked for a fixture using the "attrs-only
// component-values" feature (component tags consumed as func(gsx.Attrs)
// gsx.Node values, gated by the _gsxcompsig probe; see gen/attrtypes_test.go
// and internal/codegen's attrsonly.go). That feature does NOT exist on this
// branch: this worktree (worktree-poison-xgo) forked from main at 9b957cb,
// which predates PR #72 ("attrs-only component values", merged as 52c9750)
// — internal/codegen/attrsonly.go and the corpus's attrsonly/ cases are
// simply absent here (confirmed via `git log`/`git ls-tree`). Per the brief's
// own escalation clause ("If the attrs-only feature shape in tests is
// materially different from this description, adapt the scenario to
// exercise the real probe path and explain in your report"), this test
// instead uses the cross-package GENERIC component tag path
// (TestGenericCrossPackageTag in internal/codegen/generic_crosspkg_test.go),
// which is driven by the exact same _gsxcompsig/_gsxinferN probe machinery
// and the exact same module_importer skeleton-overlay (module_importer.go's
// key-presence check at :964) that attrs-only would have exercised. The
// safety property under test is identical: a stale poison .x.go for one
// (now-fixed) .gsx in a dependency package must never be read as
// authoritative by the probe/type-check machinery resolving a sibling
// package's cross-package reference.
//
// Fixture: package "ui" has button.gsx (a generic component, resolved
// cross-package purely through the probe/skeleton path) and icon.gsx (valid,
// but with a stale poison icon.x.go on disk simulating "fixed but not yet
// regenerated"). Sibling package "app" has post.gsx, which references
// ui.Button generically — a probe that must see ui's real skeleton (built
// from .gsx source), never the on-disk poison bytes.
func TestPoison_AttrsOnlyProbeUnaffected(t *testing.T) {
	t.Parallel()
	if testing.Short() {
		t.Skip("skipping module-resolution test in -short mode")
	}
	mod := newModule(t, "example.com/poison6")
	ui := filepath.Join(mod, "ui")
	writeFile(t, ui, "button.gsx", "package ui\n\ncomponent Button[T string | int](label T) {\n\t<button>{label}</button>\n}\n")
	writeFile(t, ui, "icon.gsx", "package ui\n\ncomponent Icon() {\n\t<span>ok</span>\n}\n")
	// Stale poison for icon.gsx, structurally matching poisonFile's real
	// output — as if a prior generate failed on icon.gsx and the user has
	// since fixed it but not regenerated.
	writeFile(t, ui, "icon.x.go", `// Code generated by gsx; DO NOT EDIT.

package ui

// GSX GENERATION FAILED for icon.gsx. Fix the errors below and re-run gsx generate.
//
//	icon.gsx:3:1: error: boom

//line icon.gsx:3:1
var _ = GSX_GENERATION_FAILED__see_icon_gsx
`)
	app := filepath.Join(mod, "app")
	writeFile(t, app, "post.gsx", "package app\n\nimport \"example.com/poison6/ui\"\n\ncomponent Post() {\n\t<ui.Button label={7} />\n}\n")

	if _, err := Generate([]string{mod}); err != nil {
		t.Fatalf("Generate failed — the cross-package probe treated ui's on-disk poison as authoritative instead of its synthetic skeleton (sticky poison): %v", err)
	}
	goBuild(t, mod)

	// The poison must have been overwritten by clean output too (Task 3/4's
	// invariant, reasserted here so a probe-path regression can't hide behind
	// a stale poison file that Generate silently left in place).
	if b, err := os.ReadFile(filepath.Join(ui, "icon.x.go")); err != nil || strings.Contains(string(b), "GSX GENERATION FAILED") {
		t.Errorf("icon.x.go poison not overwritten by clean output (err=%v)", err)
	}
}
