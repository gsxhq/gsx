func errDiag(file string, line, col int, msg string) diag.Diagnostic {
	return diag.Diagnostic{
		Start:    token.Position{Filename: file, Line: line, Column: col},
		End:      token.Position{Filename: file, Line: line, Column: col},
		Severity: diag.Error,
		Message:  msg,
		Source:   "codegen",
	}
}

// mkGsxDir creates a dir with one .gsx file and returns (dir, gsxPath).
func mkGsxDir(t *testing.T, gsxName, gsxSrc string) (string, string) {
	t.Helper()
	dir := t.TempDir()
	p := filepath.Join(dir, gsxName)
	if err := os.WriteFile(p, []byte(gsxSrc), 0o644); err != nil {
		t.Fatal(err)
	}
	return dir, p
}

func TestPoisonFile_Format(t *testing.T) {
	t.Parallel()
	dir, gsxPath := mkGsxDir(t, "app.gsx", "package ui\n\ncomponent App() {\n\t<p>{broken</p>\n}\n")
	d := errDiag(gsxPath, 12, 5, "unexpected token '<' in attribute value")
	b := poisonFile(dir, gsxPath, []diag.Diagnostic{d})
	src := string(b)

	for _, want := range []string{
		"// Code generated by gsx; DO NOT EDIT.",
		"package ui",
		"// GSX GENERATION FAILED for app.gsx. Fix the errors below and re-run gsx generate.",
		":12:5: error: unexpected token '<' in attribute value",
		"//line " + gsxPath + ":12:5",
		"var _ = GSX_GENERATION_FAILED__see_app_gsx",
	} {
		if !strings.Contains(src, want) {
			t.Errorf("poison file missing %q\n---\n%s", want, src)
		}
	}
	// Syntactically valid Go: go/parser must accept it.
	fset := token.NewFileSet()
	if _, err := parser.ParseFile(fset, "app.x.go", b, parser.ParseComments|parser.SkipObjectResolution); err != nil {
		t.Fatalf("poison file is not valid Go syntax: %v\n---\n%s", err, src)
	}
	// gofmt-clean: format.Source must be a no-op.
	formatted, err := format.Source(b)
	if err != nil {
		t.Fatalf("format.Source: %v", err)
	}
	if !bytes.Equal(formatted, b) {
		t.Errorf("poison file is not gofmt-clean:\n--- got ---\n%s\n--- formatted ---\n%s", b, formatted)
	}
}

// TestPoisonFile_MessageSanitization pins that arbitrary diagnostic-message
// content — trailing whitespace, empty messages, embedded CRLF, interior
// blank lines, NUL bytes — never produces a non-gofmt-clean or syntactically
// invalid poison file. A NUL byte would make the file invalid Go (defeating
// the LSP-safety property); trailing whitespace, empty comment lines, and
// blank interior lines would make it non-gofmt-clean (format-on-save churn).
func TestPoisonFile_MessageSanitization(t *testing.T) {
	t.Parallel()
	cases := []struct {
		name string
		msg  string
	}{
		{"trailing space", "boom "},
		{"trailing tab", "boom\t"},
		{"empty message", ""},
		{"embedded CRLF", "boom\r\nmore"},
		{"interior blank line", "a\n\nb"},
		{"NUL byte", "boom\x00more"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			t.Parallel()
			dir, gsxPath := mkGsxDir(t, "app.gsx", "package ui\n\ncomponent App() {}\n")
			d := errDiag(gsxPath, 3, 1, tc.msg)
			b := poisonFile(dir, gsxPath, []diag.Diagnostic{d})

			fset := token.NewFileSet()
			if _, err := parser.ParseFile(fset, "app.x.go", b, parser.ParseComments|parser.SkipObjectResolution); err != nil {
				t.Fatalf("poison file is not valid Go syntax: %v\n---\n%s", err, b)
			}
			formatted, err := format.Source(b)
			if err != nil {
				t.Fatalf("format.Source: %v", err)
			}
			if !bytes.Equal(formatted, b) {
				t.Errorf("poison file is not gofmt-clean:\n--- got ---\n%s\n--- formatted ---\n%s", b, formatted)
			}
		})
	}
}

func TestPoisonFile_Deterministic(t *testing.T) {
	t.Parallel()
	dir, gsxPath := mkGsxDir(t, "app.gsx", "package ui\n\ncomponent App() {}\n")
	d := errDiag(gsxPath, 3, 1, "boom")
	a := poisonFile(dir, gsxPath, []diag.Diagnostic{d})
	b := poisonFile(dir, gsxPath, []diag.Diagnostic{d})
	if !bytes.Equal(a, b) {
		t.Fatal("poison bytes are not deterministic")
	}
}

func TestPoisonFile_DirectivePassThrough(t *testing.T) {
	t.Parallel()
	dir, gsxPath := mkGsxDir(t, "app_linux.gsx",
		"//go:build linux\n\npackage ui\n\ncomponent App() {}\n")
	d := errDiag(gsxPath, 5, 1, "boom")
	src := string(poisonFile(dir, gsxPath, []diag.Diagnostic{d}))
	if !strings.Contains(src, "//go:build linux") {
		t.Errorf("poison file lost //go:build directive:\n%s", src)
	}
	// Directive must appear BEFORE the package clause.
	if strings.Index(src, "//go:build linux") > strings.Index(src, "package ui") {
		t.Errorf("//go:build must precede package clause:\n%s", src)
	}
}

func TestPoisonFile_PackageNameFallbacks(t *testing.T) {
	t.Parallel()
	// 1. Unparseable .gsx (package clause destroyed) + existing gsx-owned .x.go:
	//    package name comes from the .x.go.
	dir, gsxPath := mkGsxDir(t, "app.gsx", "pack !!! garbage\n")
	xgo := filepath.Join(dir, "app.x.go")
	if err := os.WriteFile(xgo, []byte("// Code generated by gsx; DO NOT EDIT.\n\npackage ui\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	src := string(poisonFile(dir, gsxPath, []diag.Diagnostic{errDiag(gsxPath, 1, 1, "boom")}))
	if !strings.Contains(src, "package ui") {
		t.Errorf("expected package name from existing .x.go, got:\n%s", src)
	}

	// 2. Unparseable .gsx, no .x.go, sibling .go file: name from the sibling.
	dir2, gsxPath2 := mkGsxDir(t, "app.gsx", "pack !!! garbage\n")
	if err := os.WriteFile(filepath.Join(dir2, "helper.go"), []byte("package views\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	src2 := string(poisonFile(dir2, gsxPath2, []diag.Diagnostic{errDiag(gsxPath2, 1, 1, "boom")}))
	if !strings.Contains(src2, "package views") {
		t.Errorf("expected package name from sibling .go, got:\n%s", src2)
	}

	// 3. Nothing at all: sanitized dir base name.
	dir3 := filepath.Join(t.TempDir(), "my-views")
	if err := os.MkdirAll(dir3, 0o755); err != nil {
		t.Fatal(err)
	}
	gsxPath3 := filepath.Join(dir3, "app.gsx")
	if err := os.WriteFile(gsxPath3, []byte("pack !!! garbage\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	src3 := string(poisonFile(dir3, gsxPath3, []diag.Diagnostic{errDiag(gsxPath3, 1, 1, "boom")}))
	if !strings.Contains(src3, "package my_views") {
		t.Errorf("expected sanitized dir-name package, got:\n%s", src3)
	}

	// 4. Unparseable .gsx, no .x.go, ONLY a _test.go sibling (external test
	//    package): its "package views_test" clause must never be used — fall
	//    through to the sanitized dir base name instead.
	dir4 := filepath.Join(t.TempDir(), "views")
	if err := os.MkdirAll(dir4, 0o755); err != nil {
		t.Fatal(err)
	}
	gsxPath4 := filepath.Join(dir4, "app.gsx")
	if err := os.WriteFile(gsxPath4, []byte("pack !!! garbage\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(dir4, "foo_test.go"), []byte("package views_test\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	src4 := string(poisonFile(dir4, gsxPath4, []diag.Diagnostic{errDiag(gsxPath4, 1, 1, "boom")}))
	if strings.Contains(src4, "package views_test") {
		t.Errorf("poison file must never use a _test.go sibling's package clause, got:\n%s", src4)
	}
	if !strings.Contains(src4, "package views\n") {
		t.Errorf("expected sanitized dir-name package (no non-test sibling), got:\n%s", src4)
	}
}

func TestPoisonPkgOutput_PerFileBlame(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	broken := filepath.Join(dir, "broken.gsx")
	clean := filepath.Join(dir, "clean.gsx")
	for _, p := range []string{broken, clean} {
		if err := os.WriteFile(p, []byte("package ui\n\ncomponent C() {}\n"), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	po, err := poisonPkgOutput(dir, []diag.Diagnostic{errDiag(broken, 3, 1, "boom")})
	if err != nil {
		t.Fatal(err)
	}
	if _, ok := po["broken.x.go"]; !ok {
		t.Errorf("blamed file broken.gsx not poisoned; got keys %v", keysOf(po))
	}
	if _, ok := po["clean.x.go"]; ok {
		t.Errorf("clean.gsx must NOT be poisoned on attributable blame; got keys %v", keysOf(po))
	}
}

func TestPoisonPkgOutput_UnattributableBlamesAll(t *testing.T) {
	t.Parallel()
	dir := t.TempDir()
	for _, n := range []string{"a.gsx", "b.gsx"} {
		if err := os.WriteFile(filepath.Join(dir, n), []byte("package ui\n\ncomponent C() {}\n"), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	// No filename on the diagnostic → package-level → poison every .gsx's output.
	po, err := poisonPkgOutput(dir, []diag.Diagnostic{errDiag("", 0, 0, "package-level boom")})
	if err != nil {
		t.Fatal(err)
	}
	for _, k := range []string{"a.x.go", "b.x.go"} {
		if _, ok := po[k]; !ok {
			t.Errorf("expected %s poisoned on unattributable error; got keys %v", k, keysOf(po))
		}
	}
	// Package-level poison carries the error text but no //line directive.
	if s := string(po["a.x.go"]); strings.Contains(s, "//line ") {
		t.Errorf("package-level poison must not carry a //line directive:\n%s", s)
	}
}

func TestPoisonPkgOutput_NoErrorDiags(t *testing.T) {
	t.Parallel()
	dir, gsxPath := mkGsxDir(t, "app.gsx", "package ui\n\ncomponent App() {}\n")
	w := errDiag(gsxPath, 1, 1, "just a warning")
	w.Severity = diag.Warning
	po, err := poisonPkgOutput(dir, []diag.Diagnostic{w})
	if err != nil {
		t.Fatal(err)
	}
	if len(po) != 0 {
		t.Fatalf("warnings must not poison; got keys %v", keysOf(po))
	}
}

func keysOf(po pkgOutput) []string {
	var ks []string
	for k := range po {
		ks = append(ks, k)
	}
	return ks
}
